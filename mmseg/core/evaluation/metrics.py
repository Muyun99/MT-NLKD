def f_score(precision, recall, beta=1):
    """calcuate the f-score value.

    Args:
        precision (float | torch.Tensor): The precision value.
        recall (float | torch.Tensor): The recall value.
        beta (int): Determines the weight of recall in the combined score.
            Default: False.

    Returns:
        [torch.tensor]: The f-score value.
    """
    score = (1 + beta**2) * (precision * recall) / (
        (beta**2 * precision) + recall)
    return score


def intersect_and_union(pred_label,
                        label,
                        num_classes,
                        ignore_index,
                        label_map=dict(),
                        reduce_zero_label=False):
    """Calculate intersection and Union.

    Args:
        pred_label (ndarray | str): Prediction segmentation map
            or predict result filename.
        label (ndarray | str): Ground truth segmentation map
            or label filename.
        num_classes (int): Number of categories.
        ignore_index (int): Index that will be ignored in evaluation.
        label_map (dict): Mapping old labels to new labels. The parameter will
            work only when label is str. Default: dict().
        reduce_zero_label (bool): Wether ignore zero label. The parameter will
            work only when label is str. Default: False.

     Returns:
         torch.Tensor: The intersection of prediction and ground truth
            histogram on all classes.
         torch.Tensor: The union of prediction and ground truth histogram on
            all classes.
         torch.Tensor: The prediction histogram on all classes.
         torch.Tensor: The ground truth histogram on all classes.
    """

    if isinstance(pred_label, str):
        pred_label = torch.from_numpy(np.load(pred_label))
    else:
        pred_label = torch.from_numpy((pred_label))

    if isinstance(label, str):
        label = torch.from_numpy(
            mmcv.imread(label, flag='unchanged', backend='pillow'))
    else:
        label = torch.from_numpy(label)

    if label_map is not None:
        for old_id, new_id in label_map.items():
            label[label == old_id] = new_id
    if reduce_zero_label:
        label[label == 0] = 255
        label = label - 1
        label[label == 254] = 255

    mask = (label != ignore_index)
    pred_label = pred_label[mask]
    label = label[mask]

    intersect = pred_label[pred_label == label]
    area_intersect = torch.histc(
        intersect.float(), bins=(num_classes), min=0, max=num_classes - 1)
    area_pred_label = torch.histc(
        pred_label.float(), bins=(num_classes), min=0, max=num_classes - 1)
    area_label = torch.histc(
        label.float(), bins=(num_classes), min=0, max=num_classes - 1)
    area_union = area_pred_label + area_label - area_intersect
    return area_intersect, area_union, area_pred_label, area_label


def total_intersect_and_union(results,
                              gt_seg_maps,
                              num_classes,
                              ignore_index,
                              label_map=dict(),
                              reduce_zero_label=False):
    """Calculate Total Intersection and Union.

    Args:
        results (list[ndarray] | list[str]): List of prediction segmentation
            maps or list of prediction result filenames.
        gt_seg_maps (list[ndarray] | list[str] | Iterables): list of ground
            truth segmentation maps or list of label filenames.
        num_classes (int): Number of categories.
        ignore_index (int): Index that will be ignored in evaluation.
        label_map (dict): Mapping old labels to new labels. Default: dict().
        reduce_zero_label (bool): Wether ignore zero label. Default: False.

     Returns:
         ndarray: The intersection of prediction and ground truth histogram
             on all classes.
         ndarray: The union of prediction and ground truth histogram on all
             classes.
         ndarray: The prediction histogram on all classes.
         ndarray: The ground truth histogram on all classes.
    """
    total_area_intersect = torch.zeros((num_classes, ), dtype=torch.float64)
    total_area_union = torch.zeros((num_classes, ), dtype=torch.float64)
    total_area_pred_label = torch.zeros((num_classes, ), dtype=torch.float64)
    total_area_label = torch.zeros((num_classes, ), dtype=torch.float64)
    for result, gt_seg_map in zip(results, gt_seg_maps):
        area_intersect, area_union, area_pred_label, area_label = \
            intersect_and_union(
                result, gt_seg_map, num_classes, ignore_index,
                label_map, reduce_zero_label)
        total_area_intersect += area_intersect
        total_area_union += area_union
        total_area_pred_label += area_pred_label
        total_area_label += area_label
    return total_area_intersect, total_area_union, total_area_pred_label, \
        total_area_label


def map_to_one_hot(x, num_class):
    assert x.dim() == 3
    x = x.where((x >= 0) & (x < num_class), torch.tensor([num_class], dtype=x.dtype, device=x.device))

    B, H, W = x.shape
    x_onehot = x.new_zeros((B, num_class + 1, H, W), dtype=torch.float).cuda()
    x_onehot = x_onehot.scatter_(dim=1, index=x.long().view(B, 1, H, W), value=1)[:, :-1, :, :]

    return x_onehot.contiguous()


def compute_edge(seg):
    # seg: (B, C, H, W)

    seg = seg.float()

    avgpool = torch.nn.AvgPool2d(3, stride=1, padding=1, count_include_pad=False)

    seg_avg = avgpool(seg.float())

    seg = (seg != seg_avg).float() * seg

    return seg


def dilate(seg, tol):
    # seg: (B, C, H, W)

    maxpool = torch.nn.MaxPool2d(2 * int(tol) + 1, stride=1, padding=int(tol))

    seg = maxpool(seg.float())

    return seg


def bfscore_val(results, gt_seg_maps, num_classes, ignore_index, tol=3):
    # results: (B, H, W)
    # gt_seg_maps: (B, H, W)

    N, H, W = gt_seg_maps.shape

    results = map_to_one_hot(results, num_classes)
    gt_seg_maps = map_to_one_hot(gt_seg_maps, num_classes)

    results_edge = compute_edge(results)
    gt_seg_maps_edge = compute_edge(gt_seg_maps)

    if tol > 0:
        results_dilate = dilate(results_edge, tol)
        gt_seg_maps_dilate = dilate(gt_seg_maps_edge, tol)
    else:
        results_dilate = results_edge
        gt_seg_maps_dilate = gt_seg_maps_edge

    # results_dilate: (B, C, H, W)
    # gt_seg_maps_dialte: (B, C, H, W)
    results_edge = results_edge
    gt_seg_maps_edge = gt_seg_maps_edge

    n_gt = gt_seg_maps_edge.sum(dim=3).sum(dim=2).sum(dim=0)
    n_fg = results_edge.sum(dim=3).sum(dim=2).sum(dim=0)

    match_fg = torch.sum(results_edge * gt_seg_maps_dilate, dim=3).sum(dim=2).sum(dim=0)
    match_gt = torch.sum(results_dilate * gt_seg_maps_edge, dim=3).sum(dim=2).sum(dim=0)

    p = match_fg / (n_fg + 1e-8)
    r = match_gt / n_gt

    f1 = 2 * p * r / (p + r + 1e-8)

    return f1, n_fg, n_gt, match_fg, match_gt

def class_biou(results, gt_seg_maps, num_class, radius, drop_bg=False):
    gt_seg_maps_onehot = map_to_one_hot(gt_seg_maps, num_class)

    gt_seg_maps_edge = compute_edge(gt_seg_maps_onehot)

    if radius > 0:
        gt_seg_maps_dilate = dilate(gt_seg_maps_edge, radius)
    else:
        gt_seg_maps_dilate = gt_seg_maps_edge

    # trimap: (B, H, W), binary
    # pred: (B, H, W)
    # gt_seg_maps: (B, H, W)

    if drop_bg:
        trimap = gt_seg_maps_dilate[:, 1:, :, :].max(dim=1).values.bool()
    else:
        trimap = gt_seg_maps_dilate.max(dim=1).values.bool()

    valid_mask = (gt_seg_maps >= 0) & (gt_seg_maps < num_class)
    mask = valid_mask & trimap

    label = num_class * gt_seg_maps[mask].long() + results[mask]
    count = torch.bincount(label, minlength=num_class ** 2)
    confusion_matrix = count.view(num_class, num_class)

    biou = torch.diag(confusion_matrix_boundary).to(torch.float) / (
            torch.sum(confusion_matrix_boundary, dim=1) + torch.sum(confusion_matrix_boundary, dim=0) -
            torch.diag(confusion_matrix_boundary)).to(torch.float)
    return biou

